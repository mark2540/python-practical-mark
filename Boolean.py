# boolean.py
# Demonstrates boolean variables and comparison operators

is_active = True
is_admin = False

print("is_active:", is_active)
print("is_admin:", is_admin)

p = 10
q = 20

print("p == q:", p == q)
print("p != q:", p != q)
print("p > q:", p > q)
print("p < q:", p < q)
print("p >= 10:", p >= 10)
print("q <= 20:", q <= 20)
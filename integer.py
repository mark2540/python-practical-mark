# integer.py
# Demonstrates integer variables and arithmetic operations

a = 10
b = 3

print("Addition:", a + b)        # 13
print("Subtraction:", a - b)     # 7
print("Multiplication:", a * b)  # 30
print("Division:", a / b)        # 3.333... (always returns a float)
print("Modulus:", a % b)         # 1
print("Floor Division:", a // b) # 3